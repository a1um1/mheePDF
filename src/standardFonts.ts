export function getStandardFontTextWidth(
  text: string,
  fontName: string,
  fontSize: number,
  charSpacing: number = 0,
): number {
  if (!text) return 0;
  const normalizedFont = fontName.toLowerCase();
  const isBold = normalizedFont.includes("bold");
  const isTimes = normalizedFont.includes("times") || normalizedFont.includes("roman");
  const isCourier = normalizedFont.includes("courier") || normalizedFont.includes("mono");

  // Courier is monospaced: each character is exactly 600 units
  if (isCourier) {
    return text.length * fontSize * 0.6 + text.length * charSpacing;
  }

  let widths = HELVETICA_WIDTHS;
  if (isBold) {
    if (isTimes) {
      widths = TIMES_BOLD_WIDTHS;
    } else {
      widths = HELVETICA_BOLD_WIDTHS;
    }
  } else if (isTimes) {
    widths = TIMES_ROMAN_WIDTHS;
  }

  let totalWidth = 0;
  for (let i = 0; i < text.length; i++) {
    const code = text.charCodeAt(i);
    // Standard ASCII character range
    const glyphWidth = widths[code] !== undefined ? widths[code] : 600;
    totalWidth += (glyphWidth / 1000) * fontSize + charSpacing;
  }
  return totalWidth;
}
